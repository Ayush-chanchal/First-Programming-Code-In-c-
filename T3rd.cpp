#include<iostream>
using namespace std;
int main()
{
    int a='a';
    cout<<"type casting of a : "<<a<<endl;
    char b=98;
    cout<<"type casting of b : "<<b<<endl;
    int c=9;
    int d=10;
    cout<<"add: "<<c+d<<endl;
    cout<<"sub: "<<c-d<<endl;
    cout<<"mul: "<<c*d<<endl;
    cout<<"div(in integer): "<<c/d<<endl;
    cout<<"div(in float): "<<float(c)/d<<endl;

}