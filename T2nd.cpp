#include<iostream>
using namespace std;
int main()
{
    int a=11021;
    cout<<"a:"<<a<<endl;
    char b='b';
    cout<<"b:"<<b<<endl;
    bool c=true;
    cout<<"Boolean value:"<<c<<endl;
    float f=1.2;
    cout<<"f:"<<f<<endl;
    double d=12.3;
    cout<<"d:"<<d<<endl;
    cout<<"size of a:"<<sizeof(a)<<endl;
    cout<<"size of b:"<<sizeof(b)<<endl;
    cout<<"size of c:"<<sizeof(c)<<endl;
    cout<<"size of f:"<<sizeof(f)<<endl;
    cout<<"size of d:"<<sizeof(d)<<endl;
}